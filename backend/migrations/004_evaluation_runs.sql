create table if not exists evaluation_runs (
  id uuid primary key default gen_random_uuid(),
  cases integer not null check (cases >= 0),
  recall_at_5 double precision not null,
  correct_abstention_rate double precision not null,
  citation_validity_rate double precision not null,
  average_latency_ms double precision not null,
  model text not null,
  created_at timestamptz not null default now()
);

create index if not exists evaluation_runs_created_at_idx
  on evaluation_runs(created_at desc);

create table if not exists evaluation_case_results (
  id bigint generated by default as identity primary key,
  evaluation_run_id uuid not null references evaluation_runs(id) on delete cascade,
  case_id text not null,
  question text not null,
  branch_id text not null,
  expected_documents text[] not null default '{}',
  retrieved_documents text[] not null default '{}',
  retrieval_passed boolean,
  should_abstain boolean not null,
  abstained boolean not null,
  abstention_passed boolean not null,
  citation_validity_passed boolean not null,
  cited_documents text[] not null default '{}',
  answer text not null,
  reason text,
  latency_ms integer not null,
  overall_passed boolean not null,
  unique (evaluation_run_id, case_id)
);

create index if not exists evaluation_case_results_run_idx
  on evaluation_case_results(evaluation_run_id);
